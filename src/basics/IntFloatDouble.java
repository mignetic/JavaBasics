package basics;

public class IntFloatDouble {

	public static void main(String[] args) {
		
		int zahl1 = 10 / 3;
		float zahl2 = 10f / 3f;
		double zahl3 = 10d / 3d;
		
		System.out.println("int " + zahl1);
		System.out.println("float " + zahl2);
		System.out.println("double " + zahl3);
	}
}
